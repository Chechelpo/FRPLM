-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
--  Entities
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
CREATE TABLE CHARACTERS
(
    id          INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created     TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    name        VARCHAR(255)                        NOT NULL UNIQUE,

    --Entries
    lorebook_id INT                                 NOT NULL,

    FOREIGN KEY (lorebook_id) REFERENCES LOREBOOKS (id)
);
CREATE TABLE WORLDS
(
    id               INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name             VARCHAR(255) NOT NULL UNIQUE,
    next_location_id INT          NOT NULL DEFAULT 0,

    lorebook_id      INT          NOT NULL,
    FOREIGN KEY (lorebook_id) REFERENCES LOREBOOKS (id)
);
CREATE TABLE LOCATIONS
(
    --PK
    id          INT          NOT NULL,
    world_id    INT          NOT NULL,

    --Location info
    name        VARCHAR(255) NOT NULL,
    description BLOB, --Always active when inside location
    lorebook_id INT,  --For other things

    PRIMARY KEY (world_id, id),
    FOREIGN KEY (world_id) REFERENCES WORLDS (id) ON DELETE CASCADE,
    FOREIGN KEY (lorebook_id) REFERENCES LOREBOOKS (id),
    UNIQUE (world_id, name)
);

CREATE TABLE starting_locations
(
    character_id INT     NOT NULL,
    world_id     INT     NOT NULL,
    location_id  INT     NOT NULL,

    is_static    BOOLEAN NOT NULL DEFAULT FALSE,

    reason_why   TEXT,                    --Why the character is there, not the reason why its static
    ttl          INT              DEFAULT 0,

    PRIMARY KEY (character_id, world_id), --At most one starting location per character per world
    FOREIGN KEY (character_id) REFERENCES characters (id) ON DELETE CASCADE,
    FOREIGN KEY (world_id, location_id) REFERENCES LOCATIONS (world_id, id) ON DELETE CASCADE
);

CREATE TABLE location_neighbors
(
    world_id        INT NOT NULL,
    location1_id    INT NOT NULL,
    location2_id    INT NOT NULL,

    travelCost      INT NOT NULL DEFAULT 0,
    edgeDescription TEXT,

    PRIMARY KEY (world_id, location1_id, location2_id),
    FOREIGN KEY (world_id, location1_id) REFERENCES LOCATIONS (world_id, id) ON DELETE CASCADE,
    FOREIGN KEY (world_id, location2_id) REFERENCES LOCATIONS (world_id, id) ON DELETE CASCADE
);


-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
-- Entities tags
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
CREATE TABLE CHARACTER_TAGS
(
    tag_id  INT NOT NULL,
    char_id INT NOT NULL,

    PRIMARY KEY (tag_id, char_id),
    FOREIGN KEY (tag_id) REFERENCES TAGS (id) ON DELETE CASCADE,
    FOREIGN KEY (char_id) REFERENCES CHARACTERS (id) ON DELETE CASCADE
);

CREATE TABLE LOCATION_TAGS
(
    tag_id      INT NOT NULL,
    world_id    INT NOT NULL,
    location_id INT NOT NULL,

    PRIMARY KEY (tag_id, world_id, location_id),
    FOREIGN KEY (world_id, location_id) REFERENCES LOCATIONS (world_id, id) ON DELETE CASCADE,
    FOREIGN KEY (tag_id) REFERENCES TAGS (id)
);

-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
-- Entry condition tree
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
CREATE TABLE ENTRY_CONDITION_GROUP
(
    id              INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    lorebook_id     INT      NOT NULL,
    entry_id        INT      NOT NULL,

    parent_group_id INT,

    operator        SMALLINT NOT NULL DEFAULT 0, -- AND / OR
    negated         BOOLEAN  NOT NULL DEFAULT FALSE, -- Invert conditional

    FOREIGN KEY (lorebook_id, entry_id)
        REFERENCES ENTRY (lorebook_id, entry_id)
        ON DELETE CASCADE,

    FOREIGN KEY (parent_group_id)
        REFERENCES ENTRY_CONDITION_GROUP (id)
        ON DELETE CASCADE
);

CREATE TABLE ENTRY_CONDITION
(
    id              INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    group_id        INT      NOT NULL,

    condition_type  SMALLINT NOT NULL, -- IN_LOCATION / CHARACTER_PRESENT / TAG_PRESENT / ENTITY_HAS_TAG
    target_type     SMALLINT NOT NULL, -- NONE, CHARACTER, LOCATION, TAG
    target_id       INT,
    target_world_id INT,

    negated         BOOLEAN  NOT NULL DEFAULT FALSE,

    FOREIGN KEY (group_id)
        REFERENCES ENTRY_CONDITION_GROUP (id)
        ON DELETE CASCADE
);

-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
-- Dynamic info (actual messages)
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
CREATE TABLE sessions
(
    id              INT NOT NULL GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    world_id        INT NOT NULL,
    start_time      VARCHAR(300),


    user_persona_id INT NOT NULL,           -- User character ID
    current_tick    INT NOT NULL DEFAULT 0, -- Incrementing for handing down ids to messages

    FOREIGN KEY (world_id) REFERENCES worlds (id) ON DELETE CASCADE,
    FOREIGN KEY (user_persona_id) REFERENCES characters (id) ON DELETE CASCADE
);

CREATE TABLE messages
(
    session_id       INT  NOT NULL,
    tick_num         INT  NOT NULL,

    location_id      INT  NOT NULL,

    advances_time_by INT  NOT NULL,
    user_generated   bool NOT NULL,
    content          TEXT NOT NULL,

    PRIMARY KEY (session_id, tick_num),

    FOREIGN KEY (session_id)
        REFERENCES sessions (id)
        ON DELETE CASCADE
);
-- MESSAGE TYPES ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
CREATE TABLE LLM_GEN
(
    session_id      INT      NOT NULL,
    tick_num        INT      NOT NULL,
    prompt          TEXT     NOT NULL,           -- Stored, but it could also just be regen (its deterministic)

    -- RESPONSES
    active_response SMALLINT NOT NULL DEFAULT 0, -- ID of the response that's currently active
    response_num    SMALLINT NOT NULL DEFAULT 0, -- Response num is the way I have to assign keys

    PRIMARY KEY (session_id, tick_num),
    FOREIGN KEY (session_id, tick_num) REFERENCES messages (session_id, tick_num) ON DELETE CASCADE
);

CREATE TABLE responses
(
    session_id   INT      NOT NULL,
    tick_num     INT      NOT NULL,
    response_num SMALLINT NOT NULL,
    content      TEXT     NOT NULL,

    PRIMARY KEY (session_id, tick_num, response_num),

    FOREIGN KEY (session_id, tick_num)
        REFERENCES messages (session_id, tick_num)
        ON DELETE CASCADE
);

