-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
--  Lorebook system
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
CREATE TABLE LOREBOOKS
(
    id            INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created       TIMESTAMP             DEFAULT CURRENT_TIMESTAMP NOT NULL,
    name          VARCHAR(255) NOT NULL,

    next_entry_id INT          NOT NULL DEFAULT 0
);

CREATE TABLE ENTRY
(
    lorebook_id               INT  NOT NULL,
    entry_id                  INT  NOT NULL,
    name                      VARCHAR(255),

    content                   TEXT,

    -- Injection requirements
    probability               smallint      default 100,
    outlet                    VARCHAR(255),
    delay                     INT  NOT NULL default 0,
    cooldown                  INT  NOT NULL default 0,
    stick_through             INT  NOT NULL default 0,

    -- Injection options
    injection_order           smallint      default 100,

    -- Activation strategy
    strategy                  smallint      default 0,
    embed_text                TEXT,
    prevent_further_recursion bool NOT NULL default false,
    non_recursable            bool NOT NULL default false,
    delay_until_recursion     bool NOT NULL default false,

    scan_depth                smallint      default null,


    PRIMARY KEY (lorebook_id, entry_id),
    FOREIGN KEY (lorebook_id) REFERENCES LOREBOOKS (id) ON DELETE CASCADE
);

CREATE TABLE KEYWORD
(
    id       INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    is_regex BOOL NOT NULL DEFAULT FALSE,
    keyword  VARCHAR(255) UNIQUE NOT NULL
);

CREATE TABLE ENTRY_KEYWORDS
(
    lorebook_id INT NOT NULL,
    entry_id    INT NOT NULL,
    keyword_id  INT NOT NULL,

    PRIMARY KEY (lorebook_id, entry_id, keyword_id),
    FOREIGN KEY (lorebook_id, entry_id) REFERENCES ENTRY (lorebook_id, entry_id) ON DELETE CASCADE,
    FOREIGN KEY (keyword_id) REFERENCES KEYWORD (id)
);

-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
-- TAGS
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
CREATE TABLE TAGS
(
    id    INT                 NOT NULL GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name  VARCHAR(255) UNIQUE NOT NULL,
    color CHAR(7)             NOT NULL DEFAULT '#000000'
);

-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
-- Generations
-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

CREATE TABLE CONNECTION
(
    id      INT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    host    VARCHAR(300),
    API_KEY VARBINARY(64),
    model   VARCHAR(255),
    type    VARCHAR(255)
);

CREATE TABLE COMFY_UI
(
    id        INT NOT NULL,
    host      VARCHAR(300),

    --Actual params
    workflow  JSON,
    steps     smallint     default 20,
    sampler   VARCHAR(255) default 'euler_ancestral',
    scheduler VARCHAR(255) default 'normal',
    CFG       smallint     default 6,

    FOREIGN KEY (id) REFERENCES CONNECTION (id)
);

CREATE TABLE PROMPT_TEMPLATE
(
    id                INT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    connection_id     INT,
    name              VARCHAR(300),

    --Generation config
    max_tokens        INT    DEFAULT 8192,
    temperature       float4 DEFAULT 1,
    frequency_penalty float4 default 0,
    presence_penalty  float4 default 0,
    top_p             float4 default 0.95,

    -- Backend config
    streaming         bool   default false,
    request_reasoning bool   default true,

    -- Prompt
    raw_content       BLOB,

    FOREIGN KEY (connection_id) REFERENCES CONNECTION (id)
);

CREATE TABLE MAIN_PROMPT
(
    id INT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FOREIGN KEY (id) REFERENCES PROMPT_TEMPLATE (id)
);

CREATE TABLE EXTENSION_PROMPT
(
    id INT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FOREIGN KEY (id) REFERENCES PROMPT_TEMPLATE (id)
);


